import React, { useMemo, useState } from 'react'
import { useTranslation } from 'react-i18next'
import styled from 'styled-components'
import {
  Box,
  Text,
  Toggle,
  Flex,
  ColorStyles,
  DropdownSet,
  SearchInput,
  useMatchBreakpoints,
  DropdownOption,
} from '@fingerlabs/definixswap-uikit-v2'

const ToggleWrap = styled(Flex)`
  align-items: center;
  justify-content: flex-end;
`
const SearchInputWrap = styled(Box)`
  width: 200px;
  ${({ theme }) => theme.mediaQueries.mobileXl} {
    width: 100%;
  }
`

const FarmTabButtons = ({ stackedOnly, setStackedOnly, orderBy, search, isFinished, setIsFinished }) => {
  const { t } = useTranslation()
  const { isMaxXl } = useMatchBreakpoints()

  const orderByFilter: DropdownOption[] = useMemo(() => {
    return [
      {
        id: 'sortOrder',
        label: t('Recommend'),
        orderBy: 'asc',
      },
      {
        id: 'apyValue',
        label: t('Order APR'),
        orderBy: 'desc',
      },
      {
        id: 'totalLiquidityValue',
        label: t('By Total Liquidity'),
        orderBy: 'desc',
      },
    ]
  }, [t])
  const [orderByFilterIndex, setOrderByFilterIndex] = useState(0)
  const [isOpenOrderByFilter, setIsOpenOrderByFilter] = useState(false)

  const DropdownSection = () => (
    <DropdownSet
      isOpen={isOpenOrderByFilter}
      activeIndex={orderByFilterIndex}
      options={orderByFilter}
      onButtonClick={() => setIsOpenOrderByFilter(!isOpenOrderByFilter)}
      onOptionClick={(index: number) => {
        setOrderByFilterIndex(index)
        orderBy(orderByFilter[index])
        setIsOpenOrderByFilter(false)
      }}
    />
  )
  const StakedOnlySection = () => (
    <>
      <Text textStyle="R_14R" color={ColorStyles.DEEPGREY} className="mr-s8">
        {t('Staked only Farm')}
      </Text>
      <Toggle checked={stackedOnly} onChange={() => setStackedOnly(!stackedOnly)} />
    </>
  )

  const FinishedSection = () => (
    <>
      <Text textStyle="R_14R" color={ColorStyles.DEEPGREY} className="mr-s8">
        {t('Finished')}
      </Text>
      <Toggle checked={isFinished} onChange={() => setIsFinished(!isFinished)} />
    </>
  )
  if (isMaxXl) {
    return (
      <Flex flexDirection="column">
        <Flex alignItems="center">
          <ToggleWrap>
            <StakedOnlySection />
          </ToggleWrap>
          <ToggleWrap ml="S_12">
            <FinishedSection />
          </ToggleWrap>
        </Flex>
        <Flex mt="S_12">
          <Box minWidth={148} mr="S_6">
            <DropdownSection />
          </Box>
          <SearchInput
            type="text"
            placeholder={t('Token name')}
            onSearch={(keyword) => search(keyword.trim().toLowerCase())}
            onReset={() => search('')}
          />
        </Flex>
      </Flex>
    )
  }

  return (
    <Flex flexDirection="row" justifyContent="space-between">
      <Flex justifyContent="space-between" alignItems="center">
        <Box width={148}>
          <DropdownSection />
        </Box>
        <ToggleWrap ml="S_24">
          <StakedOnlySection />
        </ToggleWrap>
        <ToggleWrap ml="S_24">
          <FinishedSection />
        </ToggleWrap>
      </Flex>
      <SearchInputWrap>
        <SearchInput
          type="text"
          placeholder={t('Token name')}
          onSearch={(keyword) => search(keyword.trim().toLowerCase())}
          onReset={() => search('')}
        />
      </SearchInputWrap>
    </Flex>
  )
}

export default FarmTabButtons
